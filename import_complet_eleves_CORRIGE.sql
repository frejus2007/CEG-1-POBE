DO $$
DECLARE
    v_academic_year_id integer;
BEGIN
    SELECT id INTO v_academic_year_id FROM public.academic_years WHERE name = '2025-2026';
    
    IF v_academic_year_id IS NOT NULL THEN
        -- 1. Supprimer les notes (grades) liées aux évaluations des classes de cette année
        DELETE FROM public.grades WHERE evaluation_id IN (
            SELECT id FROM public.evaluations WHERE class_id IN (
                SELECT id FROM public.classes WHERE academic_year_id = v_academic_year_id
            )
        );

        -- 2. Supprimer les évaluations liées à ces classes
        DELETE FROM public.evaluations WHERE class_id IN (
            SELECT id FROM public.classes WHERE academic_year_id = v_academic_year_id
        );

        -- 3. Supprimer les répartitions (teacher_assignments) liées à ces classes
        DELETE FROM public.teacher_assignments WHERE class_id IN (
            SELECT id FROM public.classes WHERE academic_year_id = v_academic_year_id
        );

        -- 4. Supprimer les coefficients (class_subjects) liés à ces classes
        DELETE FROM public.class_subjects WHERE class_id IN (
            SELECT id FROM public.classes WHERE academic_year_id = v_academic_year_id
        );

        -- 5. Supprimer les élèves liés à ces classes
        DELETE FROM public.students WHERE current_class_id IN (
            SELECT id FROM public.classes WHERE academic_year_id = v_academic_year_id
        );

        -- 6. Supprimer les classes
        DELETE FROM public.classes WHERE academic_year_id = v_academic_year_id;

        -- 7. Supprimer l'année académique
        DELETE FROM public.academic_years WHERE id = v_academic_year_id;
    END IF;
END $$;

